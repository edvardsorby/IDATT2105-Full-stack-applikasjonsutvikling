package no.ntnu.ecomback.model;

/**
 * The Category enum represents the different categories that a user of the e-commerce application can have.
 */
public enum Category {
    /**
     * An electronics category.
     */
    ELECTRONICS,

    /**
     * A vehicle category.
     */
    VEHICLE,

    /**
     * A real estate category.
     */
    REAL_ESTATE
}
